package dao;

/**
 * AbstractRiskinfo entity provides the base persistence definition of the
 * Riskinfo entity. @author MyEclipse Persistence Tools
 */

public abstract class AbstractRiskinfo implements java.io.Serializable {

	// Fields

	private Integer id;
	private String login;
	private String password;
	private String name;
	private String phone;
	private String comname;
	private Integer bidper;
	private String remain;
	private String remark;
	

	// Constructors

	/** default constructor */
	public AbstractRiskinfo() {
	}

	/** full constructor */
	public AbstractRiskinfo(String login, String password, String name,
			String phone,String comname,Integer bidper,String remain,String remark) {
		this.login = login;
		this.password = password;
		this.name = name;
		this.phone=phone;
		this.comname = comname;
		this.bidper = bidper;
		this.remain = remain;
		this.remark = remark;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getLogin() {
		return this.login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getPassword() {
		return this.password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return this.phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getComname() {
		return comname;
	}

	public void setComname(String comname) {
		this.comname = comname;
	}

	public Integer getBidper() {
		return bidper;
	}

	public void setBidper(Integer bidper) {
		this.bidper = bidper;
	}

	public String getRemain() {
		return remain;
	}

	public void setRemain(String remain) {
		this.remain = remain;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}